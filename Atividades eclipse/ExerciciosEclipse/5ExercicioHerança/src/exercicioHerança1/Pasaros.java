package exercicioHerança1;

public class Pasaros extends Animais { 
	private String nome; 
	private int idade; 
	private String voar;
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public int getIdade() {
		return idade;
	}
	public void setIdade(int idade) {
		this.idade = idade;
	}
	public String getVoar() {
		return voar;
	}
	public void setVoar(String voar) {
		this.voar = voar;
	} 
	
	

}
